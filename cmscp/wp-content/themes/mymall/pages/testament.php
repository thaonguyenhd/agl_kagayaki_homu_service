			<dd>遺贈や生前に行った贈与を計算上相続財産に加えることを「特別受益の持戻し」といいます。これは、相続人間の公平を図るための制度です。しかし持戻し免除の意思表示をすることで、持戻しをせずに残った財産を相続人の間で分けるように指定することができます。</dd>
			<dt>遺留分減殺方法の指定</dt>
			<dd>遺留分を侵害された相続人が、他の相続人や受遺者に遺留分減殺請求を行った場合に、減殺される順序や割合を指定しておくことができます。</dd>
			<dt>共同相続人間の担保責任の減免・加重</dt>
			<dd>遺産分割を行った後に、相続した財産に欠陥が見つかり、相続人が損害を受けた場合は、他の相続人はその相続を受けた割合に応じて損害を賠償しなくてはなりませんが、その義務を軽減したり加重することができます。</dd>
			<dt>遺言執行者の指定</dt>
			<dd>遺言の内容を実現するために、実際に手続を行う者を指定することができます。</dd>
