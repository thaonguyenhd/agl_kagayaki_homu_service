			<dd>相続人それぞれの相続分を、法定相続分とは異なる割合に指定することができます。</dd>
			<dt>遺産分割方法の指定</dt>
			<dd>誰に何を相続させるかを指定することができます</dd>
			<dt>遺産分割を禁止すること</dt>
			<dd>相続人が遺産分割することを最長5年間禁止することができます。</dd>
